export const ALL_SKILL_CONFIGS: SkillSeedConfig[] = [
  ...NORMAL_SKILL_CONFIGS,
  ...SPECIAL_SKILL_CONFIGS,
];

const SKILL_MAP = new Map(ALL_SKILL_CONFIGS.map((skill) => [skill.skillCode, skill]));

export function getSkillSeedConfig(skillCode?: string | null) {
  return SKILL_MAP.get(String(skillCode || '')) || null;
}

export function getLowSkillConfigs() {
  return NORMAL_SKILL_CONFIGS.filter((skill) => skill.tier === 'low');
}

export function isSpecialSkill(skill: any) {
  if (!skill) return false;
  if (skill.tier === 'special') return true;
  return getSkillSeedConfig(skill.skillCode)?.tier === 'special';
}
