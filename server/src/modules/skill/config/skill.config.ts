export type SkillTier = 'low' | 'high' | 'special';

export interface SkillSeedConfig {
  skillCode: string;
  familyCode: string;
  name: string;
  description: string;
  rarity: number;
  tier: SkillTier;
  type: string;
  category: string;
  power: number;
  triggerRate: number;
  effect: string;
  effectData: Record<string, any>;
  triggerLimit: Record<string, number>;
  conflictGroup: string;
  roleAffinity: string[];
  inheritanceWeight: number;
  canPurchase: boolean;
  canLock: boolean;
  canOverwrite: boolean;
  canInherit: boolean;
  speciesCode: string;
  enabled: boolean;
  version: string;
}

